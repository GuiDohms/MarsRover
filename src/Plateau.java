
public class Plateau {
	private int maxX;
	private int maxY;

	//creates the Plateau informing the max posx and posy
	public Plateau(int maxX, int maxY) {
		this.maxX = maxX;
		this.maxY = maxY;
	}

	public int getMaxX() {
		return this.maxX;
	}

	public int getMaxY() {
		return this.maxY;
	}
}